items = Char[]
for line ∈ eachsplit(data, keepempty = true)
    l = round(Int64, length(line) / 2)
    c = Char[]
    for i ∈ 1:l        
        contains(line[l+1:end], line[1:l][i]) && append!(c, line[1:l][i])
    end
    append!(items, unique(c))
end

priorities = Int64[]
for item ∈ items
    n = convert(Int64, item)
    if n < 97
        append!(priorities, n - 65 + 27)
    else
        append!(priorities, n - 97 + 1)
    end
end
res = sum(priorities)
